require 'rspec'
require './lib/linked_list'

describe LinkedList do

  context "#initialize" do

    it "is empty"

  end

  context "#push" do

    it "adds an element to the end of the list"

    it "returns the size of the list after adding"
  end

  context "#pop" do

    it "removes the end element"

    it "returns the element that was popped"
  end

  context "#peek" do

    it "returns the element"

    it "does not remove it"

  end

  context "#size" do

    it "returns 0 for an empty list"

    it "returns a size for a non-empty list"

  end
end
